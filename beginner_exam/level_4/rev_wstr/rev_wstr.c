#include <unistd.h>

int		main(int ac, char **av)
{
	int		i;
	int		j;

	i = 0;
	if (ac != 2)
		write(1, "\n", 1);
	else
	{
		while (av[1][i] != '\0')
			i++;
		i = i - 1;
		while (i >= 0)
		{
			while (av[1][i] >= 33 && av[1][i] <= 126)
				i--;
			j = i + 1;
			while (av[1][j] > 33 && av[1][j] <= 126)
			{
				write(1, &av[1][j], 1);
				j++;
			}
			if (i + 1 != 0)
				write(1, " ", 1);
			j = 0;
			i--;
		}
		write(1, "\n", 1);
	}
	return (0);
}
