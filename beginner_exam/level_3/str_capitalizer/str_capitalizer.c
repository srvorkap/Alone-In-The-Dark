#include <unistd.h>

int		main(int ac, char **av)
{
	int		i;
	int		j;

	j = 1;
	if (ac < 2)
		write(1, "\n", 1);
	else
	{
		while (j < ac)
		{
			if (av[j][0] >= 97 && av[j][0] <= 122)
				av[j][0] = av[j][0] - 32;
			write(1, &av[j][0], 1);
			i = 1;
			while (av[j][i] != '\0')
			{
				if ((av[j][i] >= 97 && av[j][i] <= 122) && (av[j][i - 1] == ' ' || av[j][i - 1] == '\t'))
					av[j][i] = av[j][i] - 32;
				else if ((av[j][i] >= 65 && av[j][i] <= 90) && (av[j][i - 1] != ' ' && av[j][i - 1] != '\t'))
					av[j][i] = av[j][i] + 32;
				write(1, &av[j][i], 1);
				i++;
			}
			write(1, "\n", 1);
			i = 0;
			j++;
		}
	}
	return (0);
}
