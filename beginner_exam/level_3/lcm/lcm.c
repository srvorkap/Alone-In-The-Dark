unsigned int	lcm(unsigned int a, unsigned int b)
{
	unsigned int	lcm;

	lcm = 1;
	if (a == 0 || b == 0)
		lcm = 0;
	else
	{
		while (lcm % a != 0 || lcm % b != 0)
			lcm++;
	}
	return (lcm);
}
