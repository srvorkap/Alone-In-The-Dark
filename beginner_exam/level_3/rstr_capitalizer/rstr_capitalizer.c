#include <unistd.h>

int		main(int ac, char **av)
{
	int		i;
	int		j;

	j = 1;
	if (ac < 2)
		write(1, "\n", 1);
	else
	{
		while (j < ac)
		{
			i = 0;
			while (av[j][i] != '\0')
			{
				if (av[j][i] >= 97 && av[j][i] <= 122 && (av[j][i + 1] == ' ' || av[j][i + 1] == '\t' || av[j][i + 1] == '\0'))
					av[j][i] = av[j][i] - 32;
				if (av[j][i] >= 65 && av[j][i] <= 90 && av[j][i + 1] != ' ' && av[j][i + 1] != '\t' && av[j][i + 1] != '\0')
					av[j][i] = av[j][i] + 32;
				write(1, &av[j][i], 1);
				i++;
			}
			write(1, "\n", 1);
			j++;
		}
	}
	return (0);
}
