#include <stdio.h>

void	equation(int n)
{
	int		A;
	int		B;
	int		C;

	A = 0;
	while (A < 10)
	{
		B = 0;
		while (B < 10)
		{
			C = 0;
			while (C < 10)
			{
				if (((A * 10 + B) + (C * 10 + A)) == n)
					printf("A = %d, B = %d, C = %d\n", A, B, C);
				C++;
			}
			B++;
		}
		A++;
	}
}
